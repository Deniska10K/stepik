"""
Вводятся 3 строки в случайном порядке. Напишите программу, которая выясняет можно ли из длин этих строк построить
возрастающую арифметическую прогрессию.

Формат входных данных
На вход программе подаются три строки, каждая на отдельной строке.

Формат выходных данных
Программа должна вывести строку «YES», если из длин введенных слов можно построить арифметическую прогрессию, «NO» в
ином случае.
"""

list_ = sorted((len(input()) for _ in range(3)), reverse=1)
print("YES" if list_[0] - list_[1] == list_[1] - list_[2] else "NO")
